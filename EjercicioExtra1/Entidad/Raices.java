package EjercicioExtra1.Entidad;

/**
 *
 * @author SFC
 */
public class Raices {

    public int a;
    public int b;
    public int c;

    public Raices() {
    }

    public Raices(int a, int b, int c) {
        this.a = a;
        this.b = b;
        this.c = c;
    }

    public int getA() {
        return a;
    }

    public int getB() {
        return b;
    }

    public int getC() {
        return c;
    }

}
